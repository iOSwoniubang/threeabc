#import "HXContactPerson.h"

@implementation HXContactPerson
-(id)initWithFirstPinyin:(NSString *)firstPinyin pinyin:(NSString *)pinyin name:(NSString *)name phone:(NSString *)p{
    if(self = [super init]){
        self.firstPinyin = firstPinyin;
        self.pinyin = pinyin;
        self.name = name;
        self.phone = p;
    }
    return self;
}

@end
